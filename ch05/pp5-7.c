#include <stdio.h>

int main(void) {
  int i1, i2, i3, i4, max, min;

  printf("Enter four integers : ");
  scanf("%d%d%d%d", &i1, &i2, &i3, &i4);

  if (i1 > i2) {
    max = i1;
    min = i2;
  } else {
    max = i2;
    min = i1;
  }

  if (max > i3)
    max = max;
  else
    max = i3;

  if (min < i3)
    min = min;
  else
    min = i3;

  if (max > i4)
    max = max;
  else
    max = i4;

  if (min < i4)
    min = min;
  else
    min = i4;

  printf("Largest: %d\n", max);
  printf("Smallest: %d\n", min);

  return 0;
}

