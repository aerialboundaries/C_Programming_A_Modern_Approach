// Show the output produced by each of the following program ftagments.  Assume
// that i, j, and k are int variables.
//
// (a) i = 7; j = 8;
// i *= j + 1;
// printf(("d %d, i, j);
// (b) i = j = k = 1;
// i += j += k;
// printf("%d %d %d", i, j, k);
// (c) i = 1; j = 2; k = 3;
// i -= j -= k;
// printf("%d %d %d", i, j, k);
// (d) i = 2; j = 1; k = 0;
// i *= j *= k;
// printf("%d %d %d", i, j, k);
//
// My Answers:
// (a) 57 8
// (b) 3 2 1
// (c) 2
// (d) 0 0 0
//
// ご提示いただいたC言語のコード断片の出力について、(a)と(c)の答えは間違っており、(b)と(d)の答えは正しいです。

// 各コード断片の検証
// C言語の複合代入演算子（ +=, -=,
//     *=
//     など）は、代入の前に右側の式全体を評価します。また、代入演算子は右から左へ結合するというルールが重要です。
//
//     (a) i *= j + 1;
// ステップ 式の評価 変数の値 説明 初期値
//     i = 7,
//     j = 8 1 j + 1 を評価 9 乗算される値 9 が決定。 2 i *= 9 を実行 i = 7×9 =
//             63 i = i * (j + 1) と同じ。 出力 printf("%d %d", i, j);
// i = 63,
//     j = 8
//
//     Google スプレッドシートにエクスポート 答え 正しい出力 判定 57 8 63 8
//     間違い(
//         7×9 = 63)
//
//         Google スプレッドシートにエクスポート(b) i += j += k;
// この式は、右から左に評価されます。
//
// ステップ 式の評価 変数の値 説明 初期値
//     i = 1,
//     j = 1,
//     k = 1 1 j += k を実行 j =
//         1 + 1 = 2 j の新しい値は 2。この代入の結果も 2。 2 i +=
//     (j += k) を実行 i = 1 + 2 = 3 i にステップ1の結果(2)
//                             が加算される。 出力 printf("%d %d %d", i, j, k);
// i = 3, j = 2,
//        k = 1
//
//        Google スプレッドシートにエクスポート 答え 正しい出力
//            判定 3 2 1 3 2 1 正しい
//
//                Google スプレッドシートにエクスポート(c) i -= j -= k;
// この式も右から左に評価されます。
//
// ステップ 式の評価 変数の値 説明 初期値
//     i = 1,
//     j = 2,
//     k = 3 1 j -= k を実行 j =
//         2−3 =−1 j の新しい値は −1。この代入の結果も −1。 2 i -=
//     (j -= k) を実行 i = 1−(−1) = 2 i からステップ1の結果(−1)
//         が減算される。 出力 printf("%d %d %d", i, j, k);
// i = 2, j =−1,
//        k = 3
//
//            Google スプレッドシートにエクスポート 答え 正しい出力 判定 2 2 -
//            1 3 間違い(j と k の出力が抜けている上、j の値が −1)
//
//                Google スプレッドシートにエクスポート(d) i *= j *= k;
// この式も右から左に評価されます。
//
// ステップ 式の評価 変数の値 説明 初期値
//     i = 2,
//     j = 1,
//     k = 0 1 j *= k を実行 j = 1×0 =
//         0 j の新しい値は 0。この代入の結果も 0。 2 i *= (j *= k) を実行 i =
//             2×0 = 0 i にステップ1の結果(0)
//                 が乗算される。 出力 printf("%d %d %d", i, j, k);
// i = 0, j = 0,
//        k = 0
//
//        Google スプレッドシートにエクスポート 答え 正しい出力
//            判定 0 0 0 0 0 0 正しい
//

#include <stdio.h>

int main(void) {
  int i, j, k;

  i = 7;
  j = 8;
  i *= j + 1;
  printf("%d %d\n", i, j);

  i = j = k = 1;
  i += j += k;
  printf("%d %d %d\n", i, j, k);

  i = 1;
  j = 2;
  k = 3;
  i -= j -= k;
  printf("%d %d %d\n", i, j, k);

  i = 2;
  j = 1;
  k = 0;
  i *= j *= k;
  printf("%d %d %d\n", i, j, k);
}
