int count_ones(unsigned char ch)
{
  ch = (ch & 0x55) + ((ch >> 1) & 0x55);
  ch = (ch & 0x33) + ((ch >> 2) & 0x33);
  ch = (ch & 0x0F) + ((ch >> 4) & 0x0F);
  return ch;
}

